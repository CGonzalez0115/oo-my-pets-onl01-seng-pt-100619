class Owner
  attr_accessor :pets
  attr_reader :name, :species
  @@all = []

  def initialize(species)
    @name = name
    @species = "human"
    @@all << self
  end

  def say_species
    return "I am a #{@species}."
  end

  def self.all
    @@all
  end

  def self.reset_all
    @@all.clear
  end

  def self.count
    @@all.size
  end
end
